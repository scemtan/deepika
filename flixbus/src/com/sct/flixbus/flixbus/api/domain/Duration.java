package com.sct.flixbus.flixbus.api.domain;

public class Duration {

    private int hour;
    private int minutes;

    public int getHour() {
        return hour;
    }

    public void setHour(int hour) {
        this.hour = hour;
    }

    public void setMinutes(int minutes) {
        this.minutes = minutes;
    }

    public int getMinutes() {
        return this.minutes;
    }
}
